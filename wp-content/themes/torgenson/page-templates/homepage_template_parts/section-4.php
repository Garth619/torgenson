					<span class="h2">What Are the Statute of Limitations for Personal Injury Claims in Arizona?</span>

					<p>Each state has laws in place that dictate the statute of limitations for personal injury claims — a time limit that injured parties must follow in order to file a valid personal injury claim.</p> 

					<p>In the state of Arizona, plaintiffs generally have two years to file a personal injury claim. However, the point at which this clock begins can vary on a case by case basis. For some, it may begin on the date of the accident - but in certain circumstances, it may begin on the date you discover your injury.</p>

